import 'package:flutter/services.dart';

/// Formatter that automatically converts accented Vietnamese characters to their
/// unsigned (unaccented) equivalents, and filters out non-ASCII characters or spaces.
/// Extremely useful for email/username input fields.
class UnsignedTextInputFormatter extends TextInputFormatter {
  @override
  TextEditingValue formatEditUpdate(
    TextEditingValue oldValue,
    TextEditingValue newValue,
  ) {
    int selectionOffset = newValue.selection.baseOffset;
    final StringBuffer buffer = StringBuffer();
    int newSelectionIndex = -1;

    for (int i = 0; i < newValue.text.length; i++) {
      if (i == selectionOffset) {
        newSelectionIndex = buffer.length;
      }

      final String char = newValue.text[i];
      final String unsignedChar = _removeDiacriticsChar(char);

      if (unsignedChar.isNotEmpty) {
        final codeUnit = unsignedChar.codeUnitAt(0);
        // Allow all printable ASCII characters (from '!' (33) to '~' (126))
        // This automatically excludes space (32) and any remaining non-ASCII characters
        if (codeUnit >= 33 && codeUnit <= 126) {
          buffer.write(unsignedChar);
        }
      }
    }

    if (selectionOffset == newValue.text.length) {
      newSelectionIndex = buffer.length;
    }

    final String finalText = buffer.toString();

    return TextEditingValue(
      text: finalText,
      selection: newSelectionIndex >= 0
          ? TextSelection.collapsed(offset: newSelectionIndex)
          : newValue.selection,
    );
  }

